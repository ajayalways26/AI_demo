    ACCOUNT_ID VARCHAR(64) COMMENT 'Unique identifier for the account',
    ACCOUNT_NAME VARCHAR(128) COMMENT 'Name of the account',
    ACCOUNT_NUMBER VARCHAR(64) COMMENT 'Number of the account',
    ACCOUNT_TYPE_ID BIGINT COMMENT 'Identifier for the account type',
    BUSINESS_COLUMN VARCHAR(64) COMMENT 'Business-defined attribute',
    CLOSE_DATE DATE COMMENT 'Date the account was closed',
    CREATED_BY VARCHAR(64) COMMENT 'User who created the record',
    CREATE_BATCH_ID BIGINT COMMENT 'Batch ID during creation',
    CREATE_DATE_TIME TIMESTAMP_NTZ COMMENT 'Timestamp of record creation',
