    SK_CUSTOMER_KEY BIGINT COMMENT 'Surrogate key for customer',
    SK_PRODUCT_KEY BIGINT COMMENT 'Surrogate key for product',
    STATUS_ID BIGINT COMMENT 'Foreign key for account status',
    UPDATED_BY VARCHAR(64) COMMENT 'User who last updated the record',
    UPDATE_BATCH_ID BIGINT COMMENT 'Batch ID for update events',
    UPDATE_DATE_TIME TIMESTAMP_NTZ COMMENT 'Timestamp when record was last updated',
