    SK_PRODUCT_KEY BIGINT COMMENT 'Surrogate key for the product',
    STATUS_ID BIGINT COMMENT 'Identifier for the account status',
    UPDATED_BY VARCHAR(64) COMMENT 'User who updated the record',
    UPDATE_BATCH_ID BIGINT COMMENT 'Batch ID during update',
    UPDATE_DATE_TIME TIMESTAMP_NTZ COMMENT 'Timestamp of last update',

