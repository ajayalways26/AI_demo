    ACCOUNT_ID                 VARCHAR(64)                       COMMENT 'Account identifier',
    ACCOUNT_NAME               VARCHAR(128)                      COMMENT 'Name of the account',
    ACCOUNT_NUMBER             VARCHAR(64)                       COMMENT 'The account number',
    ACCOUNT_TYPE_ID            BIGINT                            COMMENT 'Foreign key to account type',
