CREATE OR REPLACE TABLE adam_data_engg_fsi_demo.EDW_MART.DIM_ACCOUNT (
    ACCOUNT_ID VARCHAR(64) COMMENT 'Account ID of the customer',
    ACCOUNT_NAME VARCHAR(128) COMMENT 'Account Name belonging to the customer',
    ACCOUNT_NUMBER VARCHAR(64) COMMENT 'Unique Account Number',
    ACCOUNT_TYPE_ID BIGINT COMMENT 'Foreign key to ACCOUNT TYPE ID',
    BUSINESS_COLUMN VARCHAR(64) COMMENT 'Business-related information',
    CLOSE_DATE DATE COMMENT 'Account closing date',
    CREATED_BY VARCHAR(64) COMMENT 'User who created the record',
    CREATE_BATCH_ID BIGINT COMMENT 'Batch ID when record was created',
    CREATE_DATE_TIME TIMESTAMP_NTZ COMMENT 'Timestamp when record was created',
    CURRENCY_CODE VARCHAR(64) COMMENT 'Currency code used in transactions',
    CURRENT_FLAG CHAR(1) COMMENT 'Flag indicating current status',
    CUSTOMER_ID BIGINT COMMENT 'Foreign key to CUSTOMER table',
    DATA_SOURCE VARCHAR(64) COMMENT 'Source of data for the record',
    EFFECTIVE_FROM TIMESTAMP_NTZ NOT NULL COMMENT 'Start time for record becoming effective',
    EFFECTIVE_TO TIMESTAMP_NTZ COMMENT 'End time for record effectiveness',
    IS_DELETED CHAR(1) COMMENT 'Indicator if the record was deleted',
    OPEN_DATE DATE COMMENT 'Date account was opened',
    PRODUCT_ID BIGINT COMMENT 'Foreign key to PRODUCT table',
    SK_ACCOUNT_KEY BIGINT NOT NULL COMMENT 'Surrogate Key for Account',
    SK_ACCOUNT_STATUS_KEY BIGINT COMMENT 'Surrogate Key for Account Status',
    SK_ACCOUNT_TYPE_KEY BIGINT COMMENT 'Surrogate Key for Account Type',
    SK_CUSTOMER_KEY BIGINT COMMENT 'Surrogate Key for Customer',
    SK_PRODUCT_KEY BIGINT COMMENT 'Surrogate Key for Product',
    STATUS_ID BIGINT COMMENT 'Foreign key to ACCOUNT STATUS table',
    UPDATED_BY VARCHAR(64) COMMENT 'User who updated the record',
    UPDATE_BATCH_ID BIGINT COMMENT 'Batch ID when record was updated',
    UPDATE_DATE_TIME TIMESTAMP_NTZ COMMENT 'Timestamp when record was updated',

    PRIMARY KEY (SK_ACCOUNT_KEY)
) COMMENT='Dimension table for Account records';